package vip.fairy.oop.Demo6;

public class Student {

  // 定义属性
  String studentId;
  String name;
  String sex;
  int grade;
  // private int age;
  int age;

  // 定义属性“studentId”的获取方法
  public String getStudentId() {
    return studentId;
  }

  // 定义属性“studentId”的设置方法
  public void setStudentId(String student_Id) {
    studentId = student_Id;
  }

  // 定义属性“name”的获取方法
  public String getName() {
    return name;
  }

  // 定义属性“name”的设置方法
  public void setName(String student_name) {
    name = student_name;
  }

  // 定义属性“Sex”的获取方法
  public String getSex() {
    return sex;
  }

  // 定义属性“Sex”的设置方法
  public void setSex(String student_sex) {
    sex = student_sex;
  }

  // 定义属性“grade”的获取方法
  public int getGrade() {
    return grade;
  }

  // 定义属性“grade”的设置方法
  public void setGrade(int student_grade) {
    grade = student_grade;
  }

  // 定义属性“age”的获取方法
  public int getAge() {
    return age;
  }

  // 定义属性“age”的设置方法
  public void setAge(int student_age) {
    age = student_age;
  }
}
